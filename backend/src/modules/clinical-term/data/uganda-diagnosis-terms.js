const toSentenceCase = (value) => {
  const text = String(value || '').trim().replace(/\s+/g, ' ');
  return text ? text.charAt(0).toUpperCase() + text.slice(1) : '';
};

const renderPattern = (pattern, base) => pattern.replaceAll('{base}', base);

const addTerm = (items, seen, description, code = null, originRank = 0) => {
  const normalized = toSentenceCase(description);
  const key = normalized.toUpperCase();
  if (!normalized || seen.has(key)) return;
  seen.add(key);
  items.push({
    code,
    description: normalized,
    origin: 'UGANDA_CATALOG',
    rank: items.length + originRank,
  });
};

const buildUgandaDiagnosisTerms = () => {
  const items = [];
  const seen = new Set();

  explicitDiagnosisTerms.forEach(([description, code]) => {
    addTerm(items, seen, description, code, 0);
  });

  conditionGroups.forEach((group) => {
    addTerm(items, seen, group.base, group.code, 50);
    group.variants.forEach((pattern) => {
      addTerm(items, seen, renderPattern(pattern, group.base), group.code, 50);
    });
  });

  commonConditions.forEach(([base, code]) => {
    addTerm(items, seen, base, code, 200);
    commonPatterns.forEach((pattern) => {
      addTerm(items, seen, renderPattern(pattern, base), code, 200);
    });
  });

  return items.slice(0, 1000);
};

const UGANDA_DIAGNOSIS_TERMS = Object.freeze(buildUgandaDiagnosisTerms());

