const positiveNegativeOptions = ({
  positiveValue = 'POSITIVE',
  positiveLabel = 'Positive',
  positiveAliases = [],
  positiveFlag = 'POSITIVE',
  negativeValue = 'NEGATIVE',
  negativeLabel = 'Negative',
  negativeAliases = [],
  negativeFlag = 'NEGATIVE',
  includeIndeterminate = true,
}) => {
  const options = [
    resultOption({
      value: negativeValue,
      label: negativeLabel,
      aliases: negativeAliases,
      status: 'NORMAL',
      resultFlag: negativeFlag,
      isPositive: false,
    }),
    resultOption({
      value: positiveValue,
      label: positiveLabel,
      aliases: positiveAliases,
      status: 'ABNORMAL',
      resultFlag: positiveFlag,
      isPositive: true,
    }),
  ];

  if (includeIndeterminate) {
    options.push(
      resultOption({
        value: 'INDETERMINATE',
        label: 'Indeterminate',
        aliases: ['BORDERLINE'],
        status: 'ABNORMAL',
        resultFlag: 'INDETERMINATE',
      }),
      resultOption({
        value: 'INVALID',
        label: 'Invalid',
        aliases: ['REPEAT'],
        status: 'ABNORMAL',
        resultFlag: 'INVALID',
      })
    );
  }

  return options;
};

