const unitOption = (unit, label = null, ucumCode = null, isDefault = true) => ({
  label,
  unit,
  ucum_code: ucumCode,
  is_default: isDefault,
});

const referenceRange = ({
  label = null,
  unit = null,
  gender = null,
  ageMin = null,
  ageMinUnit = null,
  ageMax = null,
  ageMaxUnit = null,
  normalMin = null,
  normalMax = null,
  criticalMin = null,
  criticalMax = null,
  referenceText = null,
  notes = null,
}) => ({
  label,
  unit,
  gender,
  age_min_value: ageMin,
  age_min_unit: ageMinUnit,
  age_max_value: ageMax,
  age_max_unit: ageMaxUnit,
  normal_min_value: normalMin,
  normal_max_value: normalMax,
  critical_min_value: criticalMin,
  critical_max_value: criticalMax,
  reference_text: referenceText,
  notes,
});

const resultOption = ({
  value,
  label = null,
  aliases = [],
  status = 'ABNORMAL',
  resultFlag = null,
  isPositive = false,
}) => ({
  value,
  label,
  aliases,
  status,
  result_flag: resultFlag,
  is_positive: isPositive,
});

const numericTest = ({
  key,
  name,
  code,
  category,
  specimenType,
  description,
  referenceRange: referenceRangeText,
  referenceRanges = [],
  unit,
  unitOptions = [],
}) => ({
  key,
  name,
  code,
  category,
  specimen_type: specimenType,
  result_kind: 'NUMERIC',
  description,
  unit: unit || unitOptions[0]?.unit || null,
  unit_options:
    unitOptions.length > 0
      ? unitOptions
      : unit
        ? [unitOption(unit, null, null, true)]
        : [],
  reference_range: referenceRangeText,
  reference_ranges: referenceRanges,
  result_options: [],
});

